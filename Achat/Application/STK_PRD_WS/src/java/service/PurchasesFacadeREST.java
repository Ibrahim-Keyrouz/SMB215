package service;

import entities.Purchases;
import java.util.List;
import javax.ejb.Stateless;
import javax.persistence.EntityManager;
import javax.persistence.PersistenceContext;
import javax.ws.rs.Consumes;
import javax.ws.rs.DELETE;
import javax.ws.rs.GET;
import javax.ws.rs.POST;
import javax.ws.rs.PUT;
import javax.ws.rs.Path;
import javax.ws.rs.PathParam;
import javax.ws.rs.Produces;

/**
 *
 * @author oracle
 */
@Stateless
@Path("entities.purchases")
public class PurchasesFacadeREST extends AbstractFacade<Purchases> {
    @PersistenceContext(unitName = "STK_PRD_WSPU")
    private EntityManager em;

    public PurchasesFacadeREST() {
        super(Purchases.class);
    }

    @POST
    @Override
    @Consumes({"application/xml", "application/json"})
    public void create(Purchases entity) {
        super.create(entity);
    }

    @PUT
    @Path("{id}")
    @Consumes({"application/xml", "application/json"})
    public void edit(@PathParam("id") String id, Purchases entity) {
        super.edit(entity);
    }

    @DELETE
    @Path("{id}")
    public void remove(@PathParam("id") String id) {
        super.remove(super.find(id));
    }

    @GET
    @Path("{id}")
    @Produces({"application/xml", "application/json"})
    public Purchases find(@PathParam("id") String id) {
        return super.find(id);
    }

    @GET
    @Override
    @Produces({"application/xml", "application/json"})
    public List<Purchases> findAll() {
        return super.findAll();
    }

    @GET
    @Path("{from}/{to}")
    @Produces({"application/xml", "application/json"})
    public List<Purchases> findRange(@PathParam("from") Integer from, @PathParam("to") Integer to) {
        return super.findRange(new int[]{from, to});
    }

    @GET
    @Path("count")
    @Produces("text/plain")
    public String countREST() {
        return String.valueOf(super.count());
    }

    @Override
    protected EntityManager getEntityManager() {
        return em;
    }
    
    @GET
    @Path("/boolean_find/{id}")
    @Produces({"application/xml", "application/json"})
    public boolean boolean_find(@PathParam("id") String id) {
        Purchases a = super.find(id);
        if (a==null) return false ;
        else return true;
    }
    
}
